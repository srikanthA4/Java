public class AddFiveNumbersDirect {
    public static void main(String[] args) {
        // Declare and initialize five numbers
        int num1 = 10;
        int num2 = 20;
        int num3 = 30;
        int num4 = 15;
        int num5 = 25;

        // Calculate sum
        int sum = num1 + num2 + num3 + num4 + num5;

        // Print result
        System.out.println("Sum of the 5 numbers is: " + sum);
    }
}
