public class Arthematicoperators {
  public static void main(String[] args) {
    
   
    int a = 12, b = 5;

    //addition 
    System.out.println("a + b = " + (a + b));

    // subtraction 
    System.out.println("a - b = " + (a - b));

    // multiplication 
    System.out.println("a * b = " + (a * b));

    // division 
    System.out.println("a / b = " + (a / b));

    // modulo 
    System.out.println("a % b = " + (a % b));
  }
}